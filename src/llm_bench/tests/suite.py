def get_test(test_id: str) -> TestCase | None:
    from llm_bench.tests import FULL_TESTS

    for t in FULL_TESTS:
        if t.id == test_id:
            return t
    return None


def get_tests_by_category(category: str) -> list[TestCase]:
    from llm_bench.tests import FULL_TESTS

    return [t for t in FULL_TESTS if t.category == category]
