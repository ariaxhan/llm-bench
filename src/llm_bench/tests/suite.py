assert result[0] == {"name": "test", "type": "doc"}, f"Got {result[0]}"
assert result[1].strip() == "Hello world", f"Got '{result[1]}'"

result2 = parse_frontmatter("No frontmatter here")
assert result2[0] == {}, f"Got {result2[0]}"
assert result2[1] == "No frontmatter here"

result3 = parse_frontmatter("")
assert result3[0] == {}
assert result3[1] == ""

result4 = parse_frontmatter("---\\nonly: frontmatter\\n---")
assert result4[0] == {"only": "frontmatter"}
assert result4[1].strip() == ""

result5 = parse_frontmatter("---\\nno: closing")
